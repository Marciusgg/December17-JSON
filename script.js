function highestScore(people) {
  // Konvertuojame duomenis į masyvą su [name, points]
  const peopleArray = Object.entries(people);

  // Surušiuojame masyvą nuo didžiausio iki mažiausio
  const sortedPeople = peopleArray.sort((a, b) => b[1] - a[1]);

  // Atskiriame top 3
  const top3 = sortedPeople.slice(0, 3);

  // Grąžiname top 3
  return top3;
}

