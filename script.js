// Funkcija konvertuojanti duomenis
function formatRawData(rawData) {
  // Išskirsto duotus duomenis į linijų masyvą (/n - nauja eilutė, reiškia atskirties vietą)
  const rawDataArray = rawData.split('\n');

  // Formatuoja kiekvieną liniją į tinkamas duomenų poras
  const formattedData = rawDataArray.map(line => {
    // Suformatuojami vardai:(([A-Za-z\s.IVSrMD]+):\s*) ir taškai (\d+) į "name": points
    const match = line.match(/([A-Za-z\s.IVSrMD]+):\s*(\d+)/);
    if (match) {
      return `"${match[1]}": ${match[2]}`;
    }
    return '';
    // Sudedami kableliai
  }).filter(item => item !== '').join(', ');

  // Grąžiname suformatuotus duomenis
  return `{ ${formattedData} }`;
}

